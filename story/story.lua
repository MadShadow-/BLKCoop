##########################################################################################################################################################
	Geschichte
##########################################################################################################################################################
Helias, Salim, Pilgrim und Yuki haben Kunde erhalten, dass Erec und Drake gefangen genommen, 
dass ein fieser Baron sie gefangen genommen hat und hinrichten lassen will. Er ist für seine Grausamkeiten
bekannt und wird nicht mit sich reden lassen. Während Helias und Pilgrim aus dem Südosten kommen, erreichen
Salim und Yuki die Gegend aus dem Westen.


##########################################################################################################################################################
	Abschnitt 1: Siedlungsaufbau
##########################################################################################################################################################
	- Der Spieler startet mit einem Held, einer Handvoll Leibeigener und kann seine Burg frei platzieren
	- Banditen und Räuber machen die Wälder unsicher
	- Die Burg muss abseits der Straßen errichtet werden - die dort patroullierenden Gruppen würden sonst sofort Alarm schlagen.
	- Falls die Truppen Alarm schlagen wird der Spieler sofort von einer Horde schwerer Kavallerie attackiert.
	- Technologien anfangs stark beschränkt (nur max. stufe 2 truppen)
	- Keine Türme baubar
	- Kein Handel möglich
	
	- Es gibt Vororte in denen die Spieler Quests erledigen können und dafür im Augleich Dinge erhalten
	- Technologien, Items, Informationen
	
	
	- Ziel: Die Spieler finden sich zusammen und können den Bergpass überqueren um dann die Tore zu öffnen
	

